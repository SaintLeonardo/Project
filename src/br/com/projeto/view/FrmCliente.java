    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        // Botão buscar cliente por nome

        String nome = txtNome.getText(); //Variável que recebe o nome que será digitado
        Clientes obj = new Clientes();
        ClientesDAO dao = new ClientesDAO();

        obj = dao.consultaPorNome(nome); //Todos os dados encontrados no obj DAO serão armazenados dento do obj

        if (obj.getNome() != null) { //Retorna o objeto, porém como está nulo cai no else, onde, aparece a mensagem cliente não encontrado

            //Exibindo os dados de obj nos campos de texto
            txtCódigo.setText(String.valueOf(obj.getId()));

            obj.setNome(txtNome.getText()); //Pega o que está dentro da caixinha e armazenar dentro do atb nome do obj 

            txtNome.setText(obj.getNome());
            txtRG.setText(obj.getRg());
            txtCPF.setText(obj.getCpf());
            txtEmail.setText(obj.getEmail());
            txtTel.setText(obj.getTelefone());
            txtCelular.setText(obj.getCelular());
            txtCep.setText(obj.getCep());
            txtEndereço.setText(obj.getEndereco());
            txtNumeroCasa.setText(String.valueOf(obj.getNumero()));
            txtComplemento.setText(obj.getComplemento());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            cbUF.setSelectedItem(obj.getUf());
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
        }
    }//GEN-LAST:event_btnBuscaActionPerformed

    private void txtCepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCepKeyPressed
        // Aqui acontece a mágica

        //Programacao do keypress
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) { //Verifica se a tecla enter é apertada para que aconteça o preenchimento automático
            Clientes obj = new Clientes(); //Cria um obj
            ClientesDAO dao = new ClientesDAO(); // Cria obj DAO
            obj = dao.buscaCep(txtCep.getText()); //Faz a busca passando o CEP

            //Após a verificação de cima, preenche os campos abaixo
            
            txtEndereço.setText(obj.getEndereco());
            txtBairro.setText(obj.getBairro());
            txtCidade.setText(obj.getCidade());
            cbUF.setSelectedItem(obj.getUf());

        }
    }//GEN-LAST:event_txtCepKeyPressed


    private void btnfinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfinalizarActionPerformed
        //Variaveis que receberam os valores digitados pelo usu√°rio na tela de pagamento, respectivos campos
        double pcartao, pcheque, pdinheiro, totalpago, totalvenda, troco;

        pcartao = Double.parseDouble(txtcartao.getText());
        pcheque = Double.parseDouble(txtcheque.getText());
        pdinheiro = Double.parseDouble(txtdinheiro1.getText());

        totalvenda = Double.parseDouble(txttotal.getText());

        //Calculando total e troco
        totalpago = pcartao + pcheque + pdinheiro;
        troco = totalpago - totalvenda;
        txttroco.setText(String.valueOf(troco));//Exibindo o troco para usu√°rio

        Vendas objv = new Vendas();

        //Dados do cliente
        objv.setCliente(cliente);

        //Pega a data da venda
        Date agora = new Date();
        SimpleDateFormat dataEUA = new SimpleDateFormat("yyyy-MM-dd");//Passando a m√°scara para convers√£o internacional de data
        String datamysql = dataEUA.format(agora);

        objv.setData_venda(datamysql);

        //Total da venda e obs
        objv.setTotal_venda(totalvenda);
        objv.setObs(txtobs.getText());

        //Salvando minha venda
        VendasDAO dao_v = new VendasDAO();
        dao_v.cadastrarVendas(objv);

        //Retornar ID ultima venda
        objv.setId(dao_v.retornaUltimaVenda());

        //System.out.println("ID da √∫ltima venda: " + objv.getId());
        //Cadastrando os produtos na tabela itemVEnda
        for (int i = 0; i < carrinho.getRowCount(); i++) { //Vai ler at√© o n√∫mero de linhas existe no TableModel
            
            int qtd_estoque, qtd_comprada, qtd_atualizada;
            
            Produtos objp = new Produtos();
            ProdutosDAO dao_produto = new ProdutosDAO();
            ItemVenda item = new ItemVenda();
            item.setVenda(objv);

            objp.setId(Integer.parseInt(carrinho.getValueAt(i, 0).toString()));//Pegando o ID
            item.setProduto(objp);
            item.setQtd(Integer.parseInt(carrinho.getValueAt(i, 2).toString()));//Pegando quantidade
            item.setSubtotal(Double.parseDouble(carrinho.getValueAt(i, 4).toString()));//Pegando valor SubTotal
            
            //Baixa no estoque
            qtd_estoque = dao_produto.retornaEstoqueAtual(objp.getId());
            qtd_comprada = Integer.parseInt(carrinho.getValueAt(i, 2).toString());//qtd comprada sera igual o que tiver no carrinho
            qtd_atualizada = qtd_estoque - qtd_comprada;
            
            
            dao_produto.baixaEstoque(objp.getId(), qtd_atualizada);

            ItemVendaDAO daoitem = new ItemVendaDAO();
            daoitem.cadastraItem(item);

        }
        /****************************************************/ //Checkpoint
        JOptionPane.showMessageDialog(null, "Venda registrada com sucesso!");


    }//GEN-LAST:event_btnfinalizarActionPerformed


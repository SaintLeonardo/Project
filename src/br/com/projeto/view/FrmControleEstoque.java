    private void tabelaprodutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaprodutoMouseClicked
        //Pega os dados
        idproduto = Integer.parseInt(tabelaproduto.getValueAt(tabelaproduto.getSelectedRow(), 0).toString());
        txtdescricao1.setText(tabelaproduto.getValueAt(tabelaproduto.getSelectedRow(), 1).toString());
        
        txtestoqueatual.setText(tabelaproduto.getValueAt(tabelaproduto.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_tabelaprodutoMouseClicked

    private void btnadicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnadicionarActionPerformed
        // Adicionar estoque
        
        try {
            int qtd_estoque, qtd;
            
            qtd_estoque = Integer.parseInt(txtestoqueatual.getText());
            
            qtd = Integer.parseInt(txtqtd.getText());
            
            qtd_nova = qtd_estoque + qtd;
            
            ProdutosDAO dao = new ProdutosDAO();
            
            dao.baixaEstoque(idproduto, qtd_nova);
            
            JOptionPane.showMessageDialog(null, "Estoque do produto atualizado!");
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Selecione o produto ou informe nova quantidade: " + e);
        }
        
    }//GEN-LAST:event_btnadicionarActionPerformed


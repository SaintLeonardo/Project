    public void listar() {
        ProdutosDAO dao = new ProdutosDAO();
        List<Produtos> lista = dao.listarProdutos();
        DefaultTableModel dados = (DefaultTableModel) tabelaproduto.getModel();
        dados.setNumRows(0); //Limpa dados para garantir que não tenha nada 

        //Colocando itens da lista dentro do dados
        //Para cada item é criado uma linha 
        for (Produtos c : lista) { //Para cada item da lista, cria objeto C
            dados.addRow(new Object[]{ //Cria uma linha pra cada item
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),
                c.getFornecedor().getNome()//Recebe apenas o nome do fornecedor ao invés de todos os dados
                

            });

        }

    }
    
    
    
    
